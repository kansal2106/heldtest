const mongoose = require('mongoose');

const Schema = mongoose.Schema;

const quesSchema = new Schema({
    quesDesc: {
        type: String,
        required: true
    },
    optionA: {
        type: String,
        required: true
    },
    optionB: {
        type: String,
        required: true
    },
    test_id: {
        type: String,
        required: true
    },
    userId: {
        type: Schema.Types.ObjectId,
        ref: 'User',
        required: true
    }
});

module.exports = mongoose.model('Ques', quesSchema);